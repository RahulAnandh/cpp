#include <iostream>
using namespace std;
int main () {
    int x = 5;
    int y = 4;
    cout << "LESS THAN:"<<(x < y) << endl;
    cout << "GREATER THAN:"<<(x > y) << endl;
    cout << "EQUALS:"<<(x == y) << endl;
    cout << "NOT EQUALS:"<<(x != y) << endl;
    cout << "LESS THAN OR EQUAL:"<<(x <= y) << endl;
    cout << "GREATER THAN OR EQUAL:"<<(x >= y) << endl;
    return 0;
}